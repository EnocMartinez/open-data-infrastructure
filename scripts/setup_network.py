    hostname = os.uname().nodename
    rich.print(f"Detecting hostname...[green]{hostname}")

    rich.print("Creating docker networks...", end="")
    for network_name, network_host in conf["docker_networks"].items():
        if hostname == network_host:
            create_docker_network(network_name)
    rich.print(f"[green]ok")

    # Create the network env file
    rich.print(f"Creating odi.env file...", end="")
    create_odi_env_file(conf ["path"], conf["services"])
    rich.print("[green]ok")

    rich.print("Setting up port mappings via iptables...", end="")
    if hostname == infrastructure["entry_point"]:
        create_port_mapping(conf)
        rich.print("[green]ok")
    else:
        rich.print("[yellow]skipped")

    rich.print("\n[green]ODI network setup complete!")

    rich.print("To load new configuration run $ source /etc/environment")
