def create_port_mapping(conf):
    """
    Creates port mapping in the machine
    """

    # Check that all
    for m in conf["port_mappings"]:
        os.chdir(os.path.join(conf["path"], "odi","scripts"))
        script = "./add_nat_rule.sh"
        cmd = f'sudo {script} {m["src_port"]} {m["dst_ip"]} {m["dst_port"]} {m["protocol"]}'
        # Run script as a subprocess
        run_subprocess(cmd, allow_fail=True)


if __name__ == "__main__":
    argparser = ArgumentParser()
    argparser.add_argument("-i", "--infrastructure", help="Path no infrastructure.yaml", type=str, default="/etc/odi/infrastructure.yaml")
