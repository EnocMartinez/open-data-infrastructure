    with open(args.infrastructure) as f:
        infrastructure = yaml.safe_load(f)["infrastructure"]

    rich.print(f"Loading infrastructure.yaml...", end=False)
    conf = process_infrastructure_file(infrastructure)
    if args.verbose:
        print_infrastructure_conf(conf)
